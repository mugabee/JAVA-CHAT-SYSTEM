    private void tablesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablesMouseClicked
        try {
            DefaultTableModel model = (DefaultTableModel) tables.getModel();
            int select = tables.getSelectedRow();
            id.setText(model.getValueAt(select, 0).toString());
            tit.setText(model.getValueAt(select, 1).toString());
            auth.setText(model.getValueAt(select, 2).toString());
            home.setText(model.getValueAt(select, 3).toString());
            java.util.Date date = new SimpleDateFormat("yyy-MM-dd").parse((String)model.getValueAt(select,4));
            dat.setDate(date);
            pag.setText(model.getValueAt(select, 5).toString());
            av.setSelectedItem(model.getValueAt(select, 6));
            String code= (String) model.getValueAt(select, 7);
            //BookCategory books = (BookCategory) dao.find(code);
            //String bok = books.getName();
            cat.setSelectedItem(code);
        } catch (ParseException ex) {
            Logger.getLogger(BookView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tablesMouseClicked

