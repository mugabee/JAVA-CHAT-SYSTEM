    public void clean(){
        id.setText("");
        tit.setText("");
        auth.setText("");
        home.setText("");
        dat.setDate(null);
        pag.setText("");
        av.setSelectedItem(null);
        cat.setSelectedItem(null);
    }
    public Book send(){
        String bookid = id.getText();
        String title =tit.getText();
        String author =auth.getText();
        String publishinghouse = home.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("yyy-MM-dd");
        String sqldate = sdf.format(dat.getDate());
        int pages = Integer.parseInt(pag.getText());
        boolean b = (boolean) av.getSelectedItem();
        String boks = (String) cat.getSelectedItem();
        BookCategory books = library.findBookCategory(boks);
        String bok = books.getCode();
        BookCategory book = new BookCategory(bok);
        Book bb = new Book(bookid, title, publishinghouse, sqldate, author, pages, b, book);
        return bb;
    }
    

